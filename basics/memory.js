// Datatypes are mainly of two kinds : primitive & non-primitive.

// Primitive (Called by value) -> 7 : String, Number, Boolean, null, undefined, Symbol, BigInt
// Non-Primitive (Called by reference) -> Array, Objects, Functions

// JS is a dynamatically typed language -> We never say to define a value in specific type.

// Primitive datatypes are stored in Stack, 
// whereas Non-Primitive are stored in Heap (variables are in stack containing referece).

/*

Return type of variables in JavaScript
1) Primitive Datatypes
       Number => number
       String  => string
       Boolean  => boolean
       null  => object
       undefined  =>  undefined
       Symbol  =>  symbol
       BigInt  =>  bigint

2) Non-primitive Datatypes
       Arrays  =>  object
       Function  =>  function
       Object  =>  object

*/